class Operation
{
   public static void main(String args[])
   {
       int x=10;
       int y=5;
       System.out.println("x + y = "+ (x+y));  //Addition
       System.out.println("x - y = "+ (x-y));   //Subtraction
       System.out.println("x * y = "+ (x*y));   //Multiplication
       System.out.println("x % y = "+ (x%y));   //Division
   }
}
